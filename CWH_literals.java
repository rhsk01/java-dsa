package com.company;

public class CWH_literals {
    public static void main(String[] args) {
        int age = 35;//integer literal
        byte age2= 56; //integer literal
        short age3= 65;//integer literal
        long age4 = 666666666666666l;//long literal l must be used after this
        boolean bg = true;
        float age5 = 5.6677777788888f;
        short age7 = 66;//short literal
        char ch = 'R';
        System.out.println(age-age2-age3-age4-age5-age7);
        System.out.println(bg);
        System.out.println(ch);
    }
}
