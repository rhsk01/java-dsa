package com.company;

public class cwh_13_strings {
    public static void main(String[] args) {
      //  String name = new String("raja");
      //  System.out.println(name);
        int a = 6;
        float b = 5.6454f;
        System.out.printf("the value of a is %d and the value of b is %f" ,a,b);
        System.out.printf("the value of a is %d and the value of b is %7.1f" ,a,b);//will take 7 space and will print till 1 decimal only
        //%d is format specifier and is used for int,%f for floats,%c for char ,%s for string
    }
}
